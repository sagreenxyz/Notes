<section>
	<h2>Chapter 10 - Questions</h2>
	<ol>
		<li>
			<p>Muscle that has a striped appearance is described as being ________.</p>
			<ul>
				<li>striated</li>
				<li>nonstriated</li>
				<li>elastic</li>
				<li>excitable</li>
			</ul>
			<p><strong>Correct Answer:</strong> striated</p>
			<p><em>Explanation:</em> Striated muscles, such as skeletal and cardiac muscles, have a banded appearance due to the arrangement of actin and myosin filaments.</p>
		</li>
		<li>
			<p>Which of the following properties is not common to all three muscle tissues?</p>
			<ul>
				<li>at rest, uses shielding proteins to cover actin-binding sites</li>
				<li>excitability</li>
				<li>elasticity</li>
				<li>the need for ATP</li>
			</ul>
			<p><strong>Correct Answer:</strong> at rest, uses shielding proteins to cover actin-binding sites</p>
			<p><em>Explanation:</em> Only skeletal muscle uses shielding proteins (tropomyosin and troponin) to cover actin-binding sites at rest.</p>
		</li>
		<li>
			<p>Which of the following statements describes smooth muscle cells?</p>
			<ul>
				<li>They have a rapid onset of contractions.</li>
				<li>They are resistant to fatigue.</li>
				<li>They primarily use anaerobic metabolism.</li>
				<li>They can exhibit tetanus.</li>
			</ul>
			<p><strong>Correct Answer:</strong> They are resistant to fatigue.</p>
			<p><em>Explanation:</em> Smooth muscle cells are resistant to fatigue and can sustain long contractions without tiring.</p>
		</li>
		<li>
			<p>The correct order for the smallest to the largest unit of organization in muscle tissue is ________.</p>
			<ul>
				<li>myofibril, muscle fiber, filament, fascicle</li>
				<li>filament, myofibril, muscle fiber, fascicle</li>
				<li>fascicle, filament, muscle fiber, myofibril</li>
				<li>muscle fiber, fascicle, filament, myofibril</li>
			</ul>
			<p><strong>Correct Answer:</strong> filament, myofibril, muscle fiber, fascicle</p>
			<p><em>Explanation:</em> The correct order is filament, myofibril, muscle fiber, fascicle, from smallest to largest unit of organization in muscle tissue.</p>
		</li>
		<li>
			<p>Depolarization of the sarcolemma means ________.</p>
			<ul>
				<li>the inside of the membrane has become more negative as sodium ions accumulate</li>
				<li>the sarcolemma has completely lost any electrical charge</li>
				<li>the inside of the membrane has become less negative as sodium ions accumulate</li>
				<li>the outside of the membrane has become less negative as sodium ions accumulate</li>
			</ul>
			<p><strong>Correct Answer:</strong> the inside of the membrane has become less negative as sodium ions accumulate</p>
			<p><em>Explanation:</em> Depolarization occurs when sodium ions enter the muscle cell, making the inside of the membrane less negative.</p>
		</li>
		<li>
			<p>Which cell type helps to repair injured muscle fibers?</p>
			<ul>
				<li>myoblast cells</li>
				<li>ganglion cells</li>
				<li>satellite cells</li>
				<li>myotube cells</li>
			</ul>
			<p><strong>Correct Answer:</strong> satellite cells</p>
			<p><em>Explanation:</em> Satellite cells are involved in the repair and regeneration of injured muscle fibers.</p>
		</li>
		<li>
			<p>In relaxed muscle, the myosin-binding site on actin is blocked by ________.</p>
			<ul>
				<li>tropomyosin</li>
				<li>myoglobin</li>
				<li>titin</li>
				<li>troponin</li>
			</ul>
			<p><strong>Correct Answer:</strong> tropomyosin</p>
			<p><em>Explanation:</em> Tropomyosin blocks the myosin-binding site on actin in relaxed muscle, preventing contraction.</p>
		</li>
		<li>
			<p>Muscle relaxation occurs when ________.</p>
			<ul>
				<li>calcium ions are actively transported into the sarcoplasmic reticulum</li>
				<li>calcium ions diffuse out of the sarcoplasmic reticulum</li>
				<li>calcium ions diffuse into the sarcoplasmic reticulum</li>
				<li>calcium ions are actively transported out of the sarcoplasmic reticulum</li>
			</ul>
			<p><strong>Correct Answer:</strong> calcium ions are actively transported into the sarcoplasmic reticulum</p>
			<p><em>Explanation:</em> Muscle relaxation occurs when calcium ions are actively transported back into the sarcoplasmic reticulum, reducing the calcium concentration in the cytoplasm.</p>
		</li>
		<li>
			<p>Thin and thick filaments are organized into functional units called ________.</p>
			<ul>
				<li>myofilaments</li>
				<li>sarcomeres</li>
				<li>myofibrils</li>
				<li>T-tubules</li>
			</ul>
			<p><strong>Correct Answer:</strong> sarcomeres</p>
			<p><em>Explanation:</em> Sarcomeres are the functional units of muscle fibers, composed of organized thin and thick filaments.</p>
		</li>
		<li>
			<p>This term describes the cell structure that anchors the ends of cardiac muscle fibers to allow contraction to occur.</p>
			<ul>
				<li>epimysium</li>
				<li>desmosome</li>
				<li>endomysium</li>
				<li>calmodulin</li>
			</ul>
			<p><strong>Correct Answer:</strong> desmosome</p>
			<p><em>Explanation:</em> Desmosomes are cell structures that anchor the ends of cardiac muscle fibers, allowing them to contract together.</p>
		</li>
		<li>
			<p>During which phase of a twitch in a muscle fiber is tension the greatest?</p>
			<ul>
				<li>relaxation phase</li>
				<li>repolarization phase</li>
				<li>contraction phase</li>
				<li>resting phase</li>
			</ul>
			<p><strong>Correct Answer:</strong> contraction phase</p>
			<p><em>Explanation:</em> Tension is greatest during the contraction phase of a muscle twitch, when the muscle fibers are actively shortening.</p>
		</li>
		<li>
			<p>Muscle fatigue is caused by ________.</p>
			<ul>
				<li>buildup of ATP and pyruvic acid levels</li>
				<li>buildup of ATP and lactic acid levels</li>
				<li>exhaustion of energy reserves and buildup of lactic acid levels</li>
				<li>exhaustion of energy reserves and buildup of pyruvic acid levels</li>
			</ul>
			<p><strong>Correct Answer:</strong> exhaustion of energy reserves and buildup of lactic acid levels</p>
			<p><em>Explanation:</em> Muscle fatigue occurs when energy reserves are depleted and lactic acid levels build up, impairing muscle function.</p>
		</li>
		<li>
			<p>A sprinter would experience muscle fatigue sooner than a marathon runner due to ________.</p>
			<ul>
				<li>anaerobic metabolism in the muscles of the marathon runner</li>
				<li>glycolysis in the muscles of the marathon runner</li>
				<li>aerobic metabolism in the muscles of the sprinter</li>
				<li>anaerobic metabolism in the muscles of the sprinter</li>
			</ul>
			<p><strong>Correct Answer:</strong> anaerobic metabolism in the muscles of the sprinter</p>
			<p><em>Explanation:</em> Sprinters rely on anaerobic metabolism, which leads to quicker muscle fatigue compared to the aerobic metabolism used by marathon runners.</p>
		</li>
		<li>
			<p>What aspect of creatine phosphate allows it to supply energy to muscles?</p>
			<ul>
				<li>ATPase activity</li>
				<li>phosphate bonds</li>
				<li>carbon bonds</li>
				<li>hydrogen bonds</li>
			</ul>
			<p><strong>Correct Answer:</strong> phosphate bonds</p>
			<p><em>Explanation:</em> The high-energy phosphate bonds in creatine phosphate allow it to quickly donate a phosphate group to ADP, forming ATP and supplying energy to muscles.</p>
		</li>
		<li>
			<p>Cardiac muscles differ from skeletal muscles in that they ________.</p>
			<ul>
				<li>contain myofibrils</li>
				<li>are striated</li>
				<li>contain intercalated discs</li>
				<li>utilize aerobic metabolism</li>
			</ul>
			<p><strong>Correct Answer:</strong> contain intercalated discs</p>
			<p><em>Explanation:</em> Cardiac muscles contain intercalated discs, which are specialized connections that allow for synchronized contraction of the heart muscle.</p>
		</li>
		<li>
			<p>If cardiac muscle cells were prevented from undergoing aerobic metabolism, they ultimately would ________.</p>
			<ul>
				<li>synthesize ATP</li>
				<li>stop contracting</li>
				<li>start contracting</li>
				<li>undergo glycolysis</li>
			</ul>
			<p><strong>Correct Answer:</strong> stop contracting</p>
			<p><em>Explanation:</em> Cardiac muscle cells rely on aerobic metabolism for energy. If they are prevented from undergoing aerobic metabolism, they will eventually stop contracting.</p>
		</li>
		<li>
			<p>Smooth muscles differ from skeletal and cardiac muscles in that they ________.</p>
			<ul>
				<li>lack myosin</li>
				<li>lack myofibrils</li>
				<li>are under voluntary control</li>
				<li>lack actin</li>
			</ul>
			<p><strong>Correct Answer:</strong> lack myofibrils</p>
			<p><em>Explanation:</em> Smooth muscles lack the organized myofibrils found in skeletal and cardiac muscles, giving them a non-striated appearance.</p>
		</li>
		<li>
			<p>Which of the following statements describes fast muscle cells?</p>
			<ul>
				<li>They cannot exhibit tetanus.</li>
				<li>They primarily use amino acids.</li>
				<li>They have a slow onset of contractions.</li>
				<li>They can produce a lot of force and power for a short time.</li>
			</ul>
			<p><strong>Correct Answer:</strong> They can produce a lot of force and power for a short time.</p>
			<p><em>Explanation:</em> Fast muscle cells, also known as fast-twitch fibers, can generate a lot of force and power quickly but fatigue rapidly.</p>
		</li>
		<li>
			<p>Abnormally low muscle tone caused by the absence of low-level contractions is known as _________________. </p>
			<ul>
				<li>hyperplasia</li>
				<li>hypertonia</li>
				<li>hypertrophy</li>
				<li>hypotonia</li>
			</ul>
			<p><strong>Correct Answer:</strong> hypotonia</p>
			<p><em>Explanation:</em> Hypotonia is a condition characterized by abnormally low muscle tone and reduced strength.</p>
		</li>
		<li>
			<p>Which type of muscle tissue is responsible for involuntary movements in the walls of hollow organs?</p>
			<ul>
				<li>skeletal muscle</li>
				<li>smooth muscle</li>
				<li>cardiac muscle</li>
				<li>striated muscle</li>
			</ul>
			<p><strong>Correct Answer:</strong> smooth muscle</p>
			<p><em>Explanation:</em> Smooth muscle tissue is responsible for involuntary movements in the walls of hollow organs, such as the intestines and blood vessels.</p>
		</li>
	</ol>
