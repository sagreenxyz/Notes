		<li>
			<p>What controls whether an embryo will develop testes or ovaries?</p>
			<ul>
				<li>presence or absence of estrogen</li>
				<li>Y chromosome</li>
				<li>pituitary gland</li>
				<li>hypothalamus</li>
			</ul>
			<p><strong>Correct Answer:</strong> Y chromosome</p>
			<p><strong>Explanation:</strong> The presence of the Y chromosome, specifically the SRY gene, determines whether an embryo will develop testes. In the absence of the Y chromosome, the embryo will develop ovaries.</p>
		</li>
		<li>
			<p>What are male gametes called?</p>
			<ul>
				<li>testosterone</li>
				<li>testes</li>
				<li>sperm</li>
				<li>ova</li>
			</ul>
			<p><strong>Correct Answer:</strong> sperm</p>
			<p><strong>Explanation:</strong> Male gametes are called sperm, which are haploid cells that contribute genetic material to form an offspring.</p>
		</li>
		<li>
			<p>Without SRY expression, an embryo will develop ________.</p>
