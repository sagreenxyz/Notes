	<h2>Chapter 27 - Questions</h2>
	<ol>
			<li>
			<p>Spermatogenesis takes place in the ________.</p>
			<ul>
				<li>prostate gland</li>
				<li>ejaculatory duct</li>
				<li>glans penis</li>
				<li>seminiferous tubules</li>
			</ul>
			<p><strong>Correct Answer:</strong> seminiferous tubules</p>
			<p><strong>Explanation:</strong> Spermatogenesis is the process of sperm cell development, which occurs in the seminiferous tubules of the testes.</p>
		</li>
		<li>
			<p>The _______________ is a fluid-filled chamber that characterizes a mature tertiary follicle.</p>
			<ul>
				<li>cervix</li>
				<li>antrum</li>
				<li>ductus</li>
				<li>atrium</li>
			</ul>
			<p><strong>Correct Answer:</strong> antrum</p>
			<p><strong>Explanation:</strong> The antrum is a fluid-filled cavity that forms within a mature tertiary follicle, which is a stage in the development of an ovarian follicle.</p>
		</li>
		<li>
			<p>What are the female gonads called?</p>
			<ul>
				<li>oocytes</li>
				<li>ovaries</li>
				<li>ova</li>
				<li>oviducts</li>
			</ul>
			<p><strong>Correct Answer:</strong> ovaries</p>
			<p><strong>Explanation:</strong> The female gonads are called ovaries, which produce oocytes (egg cells) and secrete hormones such as estrogen and progesterone.</p>
		</li>
		<li>
			<p>From what structure does the corpus luteum originate?</p>
			<ul>
				<li>uterine corpus</li>
				<li>fallopian tube</li>
				<li>corpus albicans</li>
				<li>dominant follicle</li>
			</ul>
			<p><strong>Correct Answer:</strong> dominant follicle</p>
			<p><strong>Explanation:</strong> The corpus luteum originates from the dominant follicle after ovulation. It secretes hormones that support pregnancy if fertilization occurs.</p>
		</li>
		<li>
			<p>What is the function of the epididymis?</p>
			<ul>
				<li>sperm maturation and storage</li>
				<li>produces the bulk of seminal fluid</li>
				<li>provides nitric oxide needed for erections</li>
				<li>spermatogenesis</li>
			</ul>
			<p><strong>Correct Answer:</strong> sperm maturation and storage</p>
			<p><strong>Explanation:</strong> The epididymis is a coiled tube located on the back of the testis where sperm mature and are stored until ejaculation.</p>
		</li>
		<li>
			<p>Leydig cells ________.</p>
			<ul>
				<li>secrete seminal fluid</li>
				<li>secrete testosterone</li>
				<li>support spermatogenesis</li>
				<li>activate the sperm flagellum</li>
			</ul>
			<p><strong>Correct Answer:</strong> secrete testosterone</p>
			<p><strong>Explanation:</strong> Leydig cells, located in the testes, secrete testosterone, which is essential for the development of male secondary sexual characteristics and spermatogenesis.</p>
		</li>
		<li>
			<p>Where does fertilization of the egg by the sperm typically occur?</p>
			<ul>
				<li>ovary</li>
				<li>vagina</li>
				<li>uterine tube</li>
				<li>uterus</li>
			</ul>
			<p><strong>Correct Answer:</strong> uterine tube</p>
			<p><strong>Explanation:</strong> Fertilization typically occurs in the uterine tube (fallopian tube), where the sperm meets and fertilizes the egg.</p>
		</li>
		<li>
			<p>Spermatogenesis begins with mitotic division of spermatogonia (stem cells) to produce primary spermatocytes that undergo Meiosis I to create ____ secondary spermatocytes.</p>
			<ul>
				<li>1</li>
				<li>2</li>
				<li>4</li>
				<li>3</li>
			</ul>
			<p><strong>Correct Answer:</strong> 2</p>
			<p><strong>Explanation:</strong> Spermatogenesis begins with the mitotic division of spermatogonia to produce primary spermatocytes, which then undergo Meiosis I to create two secondary spermatocytes.</p>
		</li>
		<li>
			<p>The ___________________ glands are glands that secrete a lubricating mucus that cleans and lubricates the urethra prior to and during ejaculation.</p>
			<ul>
				<li>bulbourethral</li>
				<li>corpus</li>
				<li>ductus</li>
				<li>urinary</li>
			</ul>
			<p><strong>Correct Answer:</strong> bulbourethral</p>
			<p><strong>Explanation:</strong> The bulbourethral glands secrete a lubricating mucus that cleans and lubricates the urethra prior to and during ejaculation.</p>
		</li>
		<li>
			<p>Which hypothalamic hormone contributes to the regulation of the male reproductive system?</p>
			<ul>
				<li>follicle-stimulating hormone</li>
				<li>gonadotropin-releasing hormone</li>
				<li>androgens</li>
				<li>luteinizing hormone</li>
			</ul>
			<p><strong>Correct Answer:</strong> gonadotropin-releasing hormone</p>
			<p><strong>Explanation:</strong> Gonadotropin-releasing hormone (GnRH) is a hypothalamic hormone that regulates the release of follicle-stimulating hormone (FSH) and luteinizing hormone (LH) from the pituitary gland, which in turn regulate the male reproductive system.</p>
		</li>
		<li>
			<p>Why do estrogen levels fall after menopause?</p>
			<ul>
				<li>The pituitary secretes a menopause-specific hormone.</li>
				<li>The ovaries degrade.</li>
				<li>The cells of the endometrium degenerate.</li>
				<li>There are no follicles left to produce estrogen.</li>
			</ul>
			<p><strong>Correct Answer:</strong> There are no follicles left to produce estrogen.</p>
			<p><strong>Explanation:</strong> After menopause, there are no follicles left in the ovaries to produce estrogen, leading to a decline in estrogen levels.</p>
		</li>
		<li>
			<p>When do the oogonia undergo mitosis?</p>
			<ul>
				<li>before birth</li>
				<li>during fertilization</li>
				<li>at puberty</li>
				<li>at the beginning of each menstrual cycle</li>
			</ul>
			<p><strong>Correct Answer:</strong> before birth</p>
			<p><strong>Explanation:</strong> Oogonia undergo mitosis before birth to produce primary oocytes, which remain dormant until puberty.</p>
