	<section>
		<h2>Chapter 3 - Questions</h2>
		<ol>
			<li>
				<p>The diffusion of substances within a solution tends to move those substances ________ their concentration gradient.</p>
				<ul>
					<li>up</li>
					<li>down</li>
					<li>against</li>
					<li>with</li>
				</ul>
				<p><strong>Correct Answer:</strong> down</p>
				<p><em>Explanation:</em> Diffusion is the passive movement of molecules from an area of higher concentration to an area of lower concentration.</p>
			</li>
			<li>
				<p>Ion pumps and phagocytosis are both examples of ________.</p>
				<ul>
					<li>endocytosis</li>
					<li>facilitated transport</li>
					<li>active transport</li>
					<li>passive transport</li>
				</ul>
				<p><strong>Correct Answer:</strong> active transport</p>
				<p><em>Explanation:</em> Active transport requires energy to move substances against their concentration gradient or to engulf particles.</p>
			</li>
			<li>
				<p>The rough ER has its name due to what associated structures?</p>
				<ul>
					<li>Ribosomes</li>
					<li>lysosomes</li>
					<li>proteins</li>
					<li>Golgi apparatus</li>
				</ul>
				<p><strong>Correct Answer:</strong> Ribosomes</p>
				<p><em>Explanation:</em> The rough endoplasmic reticulum (ER) is studded with ribosomes, which are involved in protein synthesis.</p>
			</li>
			<li>
				<p>Which of the following is a feature common to all three components of the cytoskeleton?</p>
				<ul>
					<li>They all help the cell resist compression and tension.</li>
					<li>They are all polymers of protein subunits.</li>
					<li>They all serve to scaffold the organelles within the cell.</li>
					<li>They are all characterized by roughly the same diameter.</li>
				</ul>
				<p><strong>Correct Answer:</strong> They are all polymers of protein subunits.</p>
				<p><em>Explanation:</em> The cytoskeleton is composed of microfilaments, intermediate filaments, and microtubules, all of which are made of protein subunits.</p>
			</li>
			<li>
				<p>The nucleus and mitochondria share which of the following features?</p>
				<ul>
					<li>protein-lined membrane pores</li>
					<li>the production of cellular energy</li>
					<li>the synthesis of ribosomes</li>
					<li>a double cell membrane</li>
				</ul>
				<p><strong>Correct Answer:</strong> a double cell membrane</p>
				<p><em>Explanation:</em> Both the nucleus and mitochondria are surrounded by a double membrane, which separates their internal environment from the cytoplasm.</p>
			</li>
			<li>
				<p>Which of the following sequences on a DNA molecule would be complementary to GCTTATAT?</p>
				<ul>
					<li>CGAATATA</li>
					<li>TGCCTCTC</li>
					<li>ATCCGCGC</li>
					<li>TAGGCGCG</li>
				</ul>
				<p><strong>Correct Answer:</strong> CGAATATA</p>
				<p><em>Explanation:</em> In DNA, adenine (A) pairs with thymine (T), and cytosine (C) pairs with guanine (G).</p>
			</li>
			<li>
				<p>Which of the following phases is characterized by preparation for DNA synthesis?</p>
				<ul>
					<li>G0</li>
					<li>G1</li>
					<li>S</li>
					<li>Interphase</li>
				</ul>
				<p><strong>Correct Answer:</strong> G1</p>
				<p><em>Explanation:</em> The G1 phase is the first stage of interphase, during which the cell grows and prepares for DNA replication.</p>
			</li>
			<li>
				<p>How many “letters” of an RNA molecule, in sequence, does it take to provide the code for a single amino acid?</p>
				<ul>
					<li>1</li>
					<li>2</li>
					<li>3</li>
					<li>4</li>
				</ul>
				<p><strong>Correct Answer:</strong> 3</p>
				<p><em>Explanation:</em> A codon, which consists of three nucleotides, specifies a single amino acid during protein synthesis.</p>
			</li>
			<li>
				<p>Which of the following are not one of the main types of RNA involved in protein synthesis?</p>
				<ul>
					<li>tRNA</li>
					<li>mRNA</li>
					<li>rRNA</li>
					<li>fRNA</li>
				</ul>
				<p><strong>Correct Answer:</strong> fRNA</p>
				<p><em>Explanation:</em> The main types of RNA involved in protein synthesis are messenger RNA (mRNA), transfer RNA (tRNA), and ribosomal RNA (rRNA).</p>
			</li>
			<li>
				<p>Which of the following is part of the elongation step of DNA synthesis?</p>
				<ul>
					<li>untwisting the DNA helix</li>
					<li>pulling apart the two DNA strands</li>
					<li>attaching complementary nucleotides to the template strand</li>
					<li>none of these answers</li>
				</ul>
				<p><strong>Correct Answer:</strong> attaching complementary nucleotides to the template strand</p>
				<p><em>Explanation:</em> During elongation, DNA polymerase adds complementary nucleotides to the growing DNA strand.</p>
			</li>
			<li>
				<p>Place the following structures in order from least to most complex organization: chromatin, nucleosome, DNA, chromosome</p>
				<ul>
					<li>nucleosome, chromatin, DNA, chromosome</li>
					<li>DNA, nucleosome, chromatin, chromosome</li>
					<li>nucleosome, DNA, chromosome, chromatin</li>
					<li>DNA, chromatin, nucleosome, chromosome</li>
				</ul>
				<p><strong>Correct Answer:</strong> DNA, nucleosome, chromatin, chromosome</p>
				<p><em>Explanation:</em> DNA wraps around histone proteins to form nucleosomes, which further coil to form chromatin. Chromatin condenses to form chromosomes during cell division.</p>
			</li>
			<li>
				<p>What type of diffusion is of a substance with the aid of a membrane protein?</p>
				<ul>
					<li>ATP diffusion</li>
					<li>simple diffusion</li>
					<li>facilitated diffusion</li>
					<li>enunciated diffusion</li>
				</ul>
				<p><strong>Correct Answer:</strong> facilitated diffusion</p>
				<p><em>Explanation:</em> Facilitated diffusion involves the use of membrane proteins to help move substances across the cell membrane without the use of energy.</p>
			</li>
			<li>
				<p>What multipotent stem cells from children are sometimes banked by parents?</p>
				<ul>
					<li>embryonic stem cells</li>
					<li>cells from the umbilical cord and from baby teeth</li>
					<li>hematopoietic stem cells from red and white blood cells</li>
					<li>fetal stem cells</li>
				</ul>
				<p><strong>Correct Answer:</strong> cells from the umbilical cord and from baby teeth</p>
				<p><em>Explanation:</em> Multipotent stem cells from the umbilical cord and baby teeth can differentiate into various cell types and are often banked for potential future medical use.</p>
			</li>
			<li>
				<p>Arrange the following terms in order of increasing specialization: oligopotency, pleuripotency, unipotency, multipotency.</p>
				<ul>
					<li>oligopotency, pleuripotency, unipotency, multipotency</li>
					<li>multipotency, pleuripotency, oligopotency, unipotency</li>
					<li>pleuripotency, multipotency, oligopotency, unipotency</li>
					<li>pleuripotency, oligopotency, multipotency, unipotency</li>
				</ul>
				<p><strong>Correct Answer:</strong> pleuripotency, multipotency, oligopotency, unipotency</p>
				<p><em>Explanation:</em> Pluripotent cells can become any cell type, multipotent cells can become multiple cell types, oligopotent cells can become a few cell types, and unipotent cells are specialized to one cell type.</p>
			</li>
			<li>
				<p>Which of the following is not a difference between DNA and RNA?</p>
				<ul>
					<li>DNA contains thymine whereas RNA contains uracil</li>
					<li>RNA is single stranded and DNA is double stranded</li>
					<li>DNA contains alternating sugar-phosphate molecules whereas RNA does not contain sugars</li>
					<li>DNA contains deoxyribose and RNA contains ribose</li>
				</ul>
				<p><strong>Correct Answer:</strong> DNA contains alternating sugar-phosphate molecules whereas RNA does not contain sugars</p>
				<p><em>Explanation:</em> Both DNA and RNA contain alternating sugar-phosphate molecules in their backbone, but DNA contains deoxyribose sugar while RNA contains ribose sugar.</p>
			</li>
			<li>
				<p>What is the third stage of mitosis (and meiosis), during which sister chromatids separate into two new nuclear regions of a dividing cell?</p>
				<ul>
					<li>metaphase</li>
					<li>prophase</li>
					<li>anaphase</li>
					<li>telophase</li>
				</ul>
				<p><strong>Correct Answer:</strong> anaphase</p>
				<p><em>Explanation:</em> During anaphase, sister chromatids are pulled apart to opposite ends of the cell, ensuring each new cell will receive an identical set of chromosomes.</p>
			</li>
			<li>
				<p>What is the term used to describe the “skeleton” of a cell that is formed by rod-like proteins that support the cell’s shape and provide, among other functions, locomotive abilities?</p>
				<ul>
					<li>cytoskeleton</li>
					<li>cytoplasm</li>
					<li>nuclear envelope</li>
					<li>cytosol</li>
				</ul>
				<p><strong>Correct Answer:</strong> cytoskeleton</p>
				<p><em>Explanation:</em> The cytoskeleton is a network of protein filaments and tubules that provides structural support, shape, and movement to the cell.</p>
			</li>
			<li>
				<p>What term describes the condition of being committed to a single specialized cell type?</p>
				<ul>
					<li>multipotent</li>
					<li>unipotent</li>
					<li>pluripotent</li>
					<li>totipotent</li>
				</ul>
				<p><strong>Correct Answer:</strong> unipotent</p>
				<p><em>Explanation:</em> Unipotent cells are committed to differentiating into a single specialized cell type.</p>
			</li>
			<li>
				<p>Which of the following organelles produces large quantities of ATP when both glucose and oxygen are available to the cell?</p>
				<ul>
					<li>mitochondria</li>
					<li>lysosomes</li>
					<li>endoplasmic reticulum</li>
					<li>peroxisomes</li>
				</ul>
				<p><strong>Correct Answer:</strong> mitochondria</p>
				<p><em>Explanation:</em> Mitochondria are known as the powerhouses of the cell because they produce ATP through cellular respiration when glucose and oxygen are available.</p>
			</li>
			<li>
				<p>Which cellular organelle functions in protein synthesis?</p>
				<ul>
					<li>Golgi apparatus</li>
					<li>Nucleus</li>
					<li>Ribosome</li>
					<li>Smooth ER</li>
				</ul>
				<p><strong>Correct Answer:</strong> Ribosome</p>
				<p><em>Explanation:</em> Ribosomes are the sites of protein synthesis, where mRNA is translated into polypeptide chains.</p>
			</li>
		</ol>
