<section>
	<h2>Chapter 5 - Questions</h2>
	<ol>
		<li>
			<p>The papillary layer of the dermis is most closely associated with which layer of the epidermis?</p>
			<ul>
				<li>stratum granulosum</li>
				<li>stratum spinosum</li>
				<li>stratum corneum</li>
				<li>stratum basale</li>
			</ul>
			<p><strong>Correct Answer:</strong> stratum basale</p>
			<p><em>Explanation:</em> The papillary layer of the dermis provides nutrients and support to the stratum basale, the deepest layer of the epidermis.</p>
		</li>
		<li>
			<p>Langerhans cells are commonly found in the ________.</p>
			<ul>
				<li>stratum granulosum</li>
				<li>stratum basale</li>
				<li>stratum spinosum</li>
				<li>stratum corneum</li>
			</ul>
			<p><strong>Correct Answer:</strong> stratum spinosum</p>
			<p><em>Explanation:</em> Langerhans cells are part of the immune system and are found in the stratum spinosum, where they help protect against pathogens.</p>
		</li>
		<li>
			<p>The papillary and reticular layers of the dermis are composed mainly of ________.</p>
			<ul>
				<li>melanocytes</li>
				<li>reticocytes</li>
				<li>keratinocytes</li>
				<li>connective tissue</li>
			</ul>
			<p><strong>Correct Answer:</strong> connective tissue</p>
			<p><em>Explanation:</em> The dermis is primarily made up of connective tissue, which provides strength and elasticity to the skin.</p>
		</li>
		<li>
			<p>Collagen lends ________ to the skin.</p>
			<ul>
				<li>color</li>
				<li>structure</li>
				<li>UV protection</li>
				<li>elasticity</li>
			</ul>
			<p><strong>Correct Answer:</strong> structure</p>
			<p><em>Explanation:</em> Collagen is a protein that provides structure and support to the skin, making it firm and resilient.</p>
		</li>
		<li>
			<p>Which of the following is not a function of the hypodermis?</p>
			<ul>
				<li>protects underlying organs</li>
				<li>source of blood vessels in the epidermis</li>
				<li>helps maintain body temperature</li>
				<li>a site to long-term energy storage</li>
			</ul>
			<p><strong>Correct Answer:</strong> source of blood vessels in the epidermis</p>
			<p><em>Explanation:</em> The hypodermis primarily functions as insulation and padding for the body, and as a site for long-term energy storage, but it does not supply blood vessels to the epidermis.</p>
		</li>
		<li>
			<p>Which term describes the active phase of the hair growth cycle?</p>
			<ul>
				<li>anagen</li>
				<li>acne</li>
				<li>apocrine</li>
				<li>albinism</li>
			</ul>
			<p><strong>Correct Answer:</strong> anagen</p>
			<p><em>Explanation:</em> The anagen phase is the active growth phase of hair follicles, during which the hair shaft is produced.</p>
		</li>
		<li>
			<p>In response to stimuli from the sympathetic nervous system, the arrector pili ________.</p>
			<ul>
				<li>secrete sebum</li>
				<li>are glands on the skin surface</li>
				<li>are responsible for goose bumps</li>
				<li>can lead to excessive sweating</li>
			</ul>
			<p><strong>Correct Answer:</strong> are responsible for goose bumps</p>
			<p><em>Explanation:</em> The arrector pili muscles contract in response to cold or fear, causing the hair to stand up and creating goose bumps.</p>
		</li>
		<li>
			<p>Eccrine sweat glands ________.</p>
			<ul>
				<li>are present on hair</li>
				<li>act as a moisturizer</li>
				<li>produce sebum</li>
				<li>are present in the skin throughout the body and produce watery sweat</li>
			</ul>
			<p><strong>Correct Answer:</strong> are present in the skin throughout the body and produce watery sweat</p>
			<p><em>Explanation:</em> Eccrine sweat glands are distributed throughout the body and help regulate body temperature by producing a watery sweat.</p>
		</li>
		<li>
			<p>Sebaceous glands ________.</p>
			<ul>
				<li>may function in response to touch</li>
				<li>release a watery solution of salt and metabolic waste</li>
				<li>are associated with hair follicles</li>
				<li>are a type of sweat gland</li>
			</ul>
			<p><strong>Correct Answer:</strong> are associated with hair follicles</p>
			<p><em>Explanation:</em> Sebaceous glands are associated with hair follicles and secrete sebum, an oily substance that lubricates and waterproofs the skin and hair.</p>
		</li>
		<li>
			<p>After a skin injury, the body initiates a wound-healing response. The first step of this response is the formation of a blood clot to stop bleeding. Which of the following would be the next response?</p>
			<ul>
				<li>increased production of connective tissue</li>
				<li>an increased activity in the stratum lucidum</li>
				<li>an increase in Pacinian corpuscles around the wound</li>
				<li>increased production of melanin by melanocytes</li>
			</ul>
			<p><strong>Correct Answer:</strong> increased production of connective tissue</p>
			<p><em>Explanation:</em> After a blood clot forms, the next step in wound healing is the production of connective tissue to repair the damaged area.</p>
		</li>
		<li>
			<p>A thickened area of skin that arises due to constant abrasion is known as?</p>
			<ul>
				<li>bedsore</li>
				<li>catagen</li>
				<li>callus</li>
				<li>cortex</li>
			</ul>
			<p><strong>Correct Answer:</strong> callus</p>
			<p><em>Explanation:</em> A callus is a thickened area of skin that forms in response to repeated friction or pressure.</p>
		</li>
		<li>
			<p>Similar to the hair, nails grow continuously throughout our lives. Which of the following is furthest from the nail growth center?</p>
			<ul>
				<li>hyponychium</li>
				<li>nail root</li>
				<li>nail bed</li>
				<li>eponychium</li>
			</ul>
			<p><strong>Correct Answer:</strong> hyponychium</p>
			<p><em>Explanation:</em> The hyponychium is the area of skin under the free edge of the nail, furthest from the nail growth center at the nail root.</p>
		</li>
		<li>
			<p>In humans, exposure of the skin to sunlight is required for ________.</p>
			<ul>
				<li>folate production</li>
				<li>vitamin D synthesis</li>
				<li>thermoregulation</li>
				<li>arteriole constriction</li>
			</ul>
			<p><strong>Correct Answer:</strong> vitamin D synthesis</p>
			<p><em>Explanation:</em> Sunlight exposure is necessary for the skin to produce vitamin D, which is important for bone health and immune function.</p>
		</li>
		<li>
			<p>One of the functions of the integumentary system is protection. Which of the following does not directly contribute to that function?</p>
			<ul>
				<li>folic acid synthesis</li>
				<li>desmosomes</li>
				<li>stratum lucidum</li>
				<li>Merkel cells</li>
			</ul>
			<p><strong>Correct Answer:</strong> folic acid synthesis</p>
			<p><em>Explanation:</em> Folic acid synthesis does not directly contribute to the protective function of the integumentary system, whereas desmosomes, the stratum lucidum, and Merkel cells do.</p>
		</li>
		<li>
			<p>An individual using a sharp knife notices a small amount of blood where he just cut himself. Which of the following layers of skin did he have to cut into in order to bleed?</p>
			<ul>
				<li>stratum granulosum</li>
				<li>stratum basale</li>
				<li>stratum corneum</li>
				<li>papillary dermis</li>
			</ul>
			<p><strong>Correct Answer:</strong> papillary dermis</p>
			<p><em>Explanation:</em> The papillary dermis contains blood vessels, so cutting into this layer would cause bleeding.</p>
		</li>
		<li>
			<p>The stratum ______________ has a grainy appearance due to further changes to the keratinocytes as they are pushed from the stratum spinosum.</p>
			<ul>
				<li>spinosum</li>
				<li>corneum</li>
				<li>basale</li>
				<li>granulosum</li>
			</ul>
			<p><strong>Correct Answer:</strong> granulosum</p>
			<p><em>Explanation:</em> The stratum granulosum has a grainy appearance due to the accumulation of keratohyalin granules in the keratinocytes.</p>
		</li>
		<li>
			<p>As you are walking down the beach, you see a dead, dry, shriveled-up fish. Which layer of your epidermis keeps you from drying out?</p>
			<ul>
				<li>stratum corneum</li>
				<li>stratum spinosum</li>
				<li>stratum granulosum</li>
				<li>stratum basale</li>
			</ul>
			<p><strong>Correct Answer:</strong> stratum corneum</p>
			<p><em>Explanation:</em> The stratum corneum is the outermost layer of the epidermis and acts as a barrier to prevent water loss, keeping the skin hydrated.</p>
		</li>
		<li>
			<p>If you cut yourself and bacteria enter the wound, which of the following cells would help get rid of the bacteria?</p>
			<ul>
				<li>melanocytes</li>
				<li>Langerhans cells</li>
				<li>Merkel cells</li>
				<li>keratinocytes</li>
			</ul>
			<p><strong>Correct Answer:</strong> Langerhans cells</p>
			<p><em>Explanation:</em> Langerhans cells are immune cells in the epidermis that help detect and fight off pathogens, including bacteria.</p>
		</li>
		<li>
			<p>In general, skin cancers ________.</p>
			<ul>
				<li>can be reduced by limiting exposure to the sun</li>
				<li>occur due to poor hygiene</li>
				<li>are easily treatable and not a major health concern</li>
				<li>affect only the epidermis</li>
			</ul>
			<p><strong>Correct Answer:</strong> can be reduced by limiting exposure to the sun</p>
			<p><em>Explanation:</em> Limiting exposure to UV radiation from the sun can help reduce the risk of developing skin cancer.</p>
		</li>
		<li>
			<p>Squamous cell carcinomas are the second most common of the skin cancers and are capable of metastasizing if not treated. This cancer affects which cells?</p>
			<ul>
				<li>melanocytes of the stratum basale</li>
				<li>basal cells of the stratum basale</li>
				<li>Langerhans cells of the stratum lucidum</li>
				<li>keratinocytes of the stratum spinosum</li>
			</ul>
			<p><strong>Correct Answer:</strong> keratinocytes of the stratum spinosum</p>
			<p><em>Explanation:</em> Squamous cell carcinoma affects the keratinocytes in the stratum spinosum, which can become cancerous and potentially spread to other parts of the body if not treated.</p>
		</li>
	</ol>
