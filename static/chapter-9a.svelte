<section>
	<h2>Chapter 9 - Questions</h2>
	<ol>
		<li>
			<p>Which of these joints is classified as a synarthrosis?</p>
			<ul>
				<li>the pubic symphysis</li>
				<li>an intervertebral disc</li>
				<li>the shoulder joint</li>
				<li>the manubriosternal joint</li>
			</ul>
			<p><strong>Correct Answer:</strong> the manubriosternal joint</p>
			<p><em>Explanation:</em> A synarthrosis is an immovable joint, such as the manubriosternal joint, which provides stability.</p>
		</li>
		<li>
			<p>Which of these joints is classified as a biaxial diarthrosis?</p>
			<ul>
				<li>the hip joint</li>
				<li>the elbow joint</li>
				<li>the pubic symphysis</li>
				<li>the metacarpophalangeal joint</li>
			</ul>
			<p><strong>Correct Answer:</strong> the metacarpophalangeal joint</p>
			<p><em>Explanation:</em> A biaxial diarthrosis allows movement in two planes, such as the metacarpophalangeal joint, which allows flexion-extension and abduction-adduction.</p>
		</li>
		<li>
			<p>Intramembranous ossification ________.</p>
			<ul>
				<li>produces the bones of the top and sides of the skull</li>
				<li>involves the conversion of a hyaline cartilage model into bone</li>
				<li>gives rise to the bones of the limbs</li>
				<li>produces the bones of the face and base of the skull</li>
			</ul>
			<p><strong>Correct Answer:</strong> produces the bones of the top and sides of the skull</p>
			<p><em>Explanation:</em> Intramembranous ossification is the process by which flat bones, such as those of the skull, are formed directly from mesenchymal tissue.</p>
		</li>
		<li>
			<p>Which type of fibrous joint connects the tibia and fibula?</p>
			<ul>
				<li>symphysis</li>
				<li>syndesmosis</li>
				<li>suture</li>
				<li>gomphosis</li>
			</ul>
			<p><strong>Correct Answer:</strong> syndesmosis</p>
			<p><em>Explanation:</em> A syndesmosis is a type of fibrous joint where bones are connected by a ligament, such as the connection between the tibia and fibula.</p>
		</li>
		<li>
			<p>An example of a wide fibrous joint is ________.</p>
			<ul>
				<li>a synostosis</li>
				<li>a suture joint</li>
				<li>the interosseous membrane of the forearm</li>
				<li>a gomphosis</li>
			</ul>
			<p><strong>Correct Answer:</strong> the interosseous membrane of the forearm</p>
			<p><em>Explanation:</em> The interosseous membrane of the forearm is a wide fibrous joint that provides stability and allows for slight movement between the radius and ulna.</p>
		</li>
		<li>
			<p>A cartilaginous joint ________.</p>
			<ul>
				<li>is formed by a wide sheet of fibrous connective tissue</li>
				<li>is called a symphysis when the bones are united by fibrocartilage</li>
				<li>has a joint cavity</li>
				<li>anchors the teeth to the jaws</li>
			</ul>
			<p><strong>Correct Answer:</strong> is called a symphysis when the bones are united by fibrocartilage</p>
			<p><em>Explanation:</em> A cartilaginous joint is called a symphysis when the bones are united by fibrocartilage, providing strength and flexibility, such as the pubic symphysis.</p>
		</li>
		<li>
			<p>The epiphyseal plate of a growing long bone in a child is classified as a ________.</p>
			<ul>
				<li>symphysis</li>
				<li>synostosis</li>
				<li>syndesmosis</li>
				<li>synchondrosis</li>
			</ul>
			<p><strong>Correct Answer:</strong> synchondrosis</p>
			<p><em>Explanation:</em> The epiphyseal plate is a synchondrosis, a type of cartilaginous joint where bones are joined by hyaline cartilage, allowing for growth in length.</p>
		</li>
		<li>
			<p>Which type of joint provides the greatest range of motion?</p>
			<ul>
				<li>hinge</li>
				<li>condyloid</li>
				<li>ball-and-socket</li>
				<li>plane</li>
			</ul>
			<p><strong>Correct Answer:</strong> ball-and-socket</p>
			<p><em>Explanation:</em> A ball-and-socket joint, such as the hip or shoulder joint, allows for the greatest range of motion, including rotation, flexion, extension, abduction, and adduction.</p>
		</li>
		<li>
			<p>Which type of joint allows for only uniaxial movement?</p>
			<ul>
				<li>hinge joint</li>
				<li>saddle joint</li>
				<li>ball-and-socket joint</li>
				<li>condyloid joint</li>
			</ul>
			<p><strong>Correct Answer:</strong> hinge joint</p>
			<p><em>Explanation:</em> A hinge joint, such as the elbow or knee, allows for uniaxial movement, meaning movement in one plane, typically flexion and extension.</p>
		</li>
		<li>
			<p>Endochondral ossification is ________.</p>
			<ul>
				<li>completed before birth</li>
				<li>the process that replaces hyaline cartilage with bone tissue</li>
				<li>the process by which mesenchyme differentiates directly into bone tissue</li>
				<li>the process that gives rise to the joint interzone and future joint cavity</li>
			</ul>
			<p><strong>Correct Answer:</strong> the process that replaces hyaline cartilage with bone tissue</p>
			<p><em>Explanation:</em> Endochondral ossification is the process by which bone tissue replaces hyaline cartilage, forming most of the bones in the body, including long bones.</p>
		</li>
		<li>
			<p>A bursa ________.</p>
			<ul>
				<li>prevents friction between skin and bone, or a muscle tendon and bone</li>
				<li>is the strong band of connective tissue that holds bones together at a synovial joint</li>
				<li>surrounds a tendon at the point where the tendon crosses a joint</li>
				<li>secretes the lubricating fluid for a synovial joint</li>
			</ul>
			<p><strong>Correct Answer:</strong> prevents friction between skin and bone, or a muscle tendon and bone</p>
			<p><em>Explanation:</em> A bursa is a fluid-filled sac that reduces friction between tissues, such as skin and bone or muscle tendons and bone, allowing for smooth movement.</p>
		</li>
		<li>
			<p>Condyloid joints ________.</p>
			<ul>
				<li>are a uniaxial diarthrosis joint</li>
				<li>are found at the proximal radioulnar joint</li>
				<li>include the radiocarpal joint</li>
				<li>are a type of ball-and-socket joint</li>
			</ul>
			<p><strong>Correct Answer:</strong> include the radiocarpal joint</p>
			<p><em>Explanation:</em> Condyloid joints, such as the radiocarpal joint of the wrist, allow for biaxial movement, including flexion-extension and abduction-adduction.</p>
		</li>
		<li>
			<p>A meniscus is ________.</p>
			<ul>
				<li>a fluid-filled space that prevents friction between a muscle tendon and underlying bone</li>
				<li>a fibrocartilage pad that provides padding between bones</li>
				<li>the articular cartilage that covers the ends of a bone at a synovial joint</li>
				<li>the lubricating fluid within a synovial joint</li>
			</ul>
			<p><strong>Correct Answer:</strong> a fibrocartilage pad that provides padding between bones</p>
			<p><em>Explanation:</em> A meniscus is a fibrocartilage pad that provides cushioning and stability within a joint, such as the knee, reducing wear and tear on the joint surfaces.</p>
		</li>
		<li>
			<p>Which motion moves the bottom of the foot away from the midline of the body?</p>
			<ul>
				<li>elevation</li>
				<li>eversion</li>
				<li>plantar flexion</li>
				<li>dorsiflexion</li>
			</ul>
			<p><strong>Correct Answer:</strong> eversion</p>
			<p><em>Explanation:</em> Eversion is the movement that turns the sole of the foot outward, away from the midline of the body.</p>
		</li>
		<li>
			<p>Supination is the motion that moves the ________.</p>
			<ul>
				<li>foot so that the bottom of the foot faces the midline of the body</li>
				<li>hand from the palm backward position to the palm forward position</li>
				<li>hand from the palm forward position to the palm backward position</li>
				<li>scapula in an upward direction</li>
			</ul>
			<p><strong>Correct Answer:</strong> hand from the palm backward position to the palm forward position</p>
			<p><em>Explanation:</em> Supination is the rotational movement of the forearm that turns the palm to face forward or upward.</p>
		</li>
		<li>
			<p>________________ is the excessive flexion of a joint.</p>
			<ul>
				<li>Pariflexion</li>
				<li>Hyperflexion</li>
				<li>Uniflexion</li>
				<li>Hypoflexion</li>
			</ul>
			<p><strong>Correct Answer:</strong> Hyperflexion</p>
			<p><em>Explanation:</em> Hyperflexion is the excessive bending of a joint beyond its normal range of motion.</p>
		</li>
		<li>
			<p>Which region of the vertebral column has the greatest range of motion for rotation?</p>
			<ul>
				<li>lumbar</li>
				<li>cervical</li>
				<li>thoracic</li>
				<li>sacral</li>
			</ul>
			<p><strong>Correct Answer:</strong> thoracic</p>
			<p><em>Explanation:</em> The thoracic region of the vertebral column allows for the greatest range of rotational movement due to the orientation of the facet joints.</p>
		</li>
		<li>
			<p>Synovial joints ________.</p>
			<ul>
				<li>are produced by intramembranous ossification</li>
				<li>are derived from fontanelles</li>
				<li>are produced by endochondral ossification</li>
				<li>develop at an interzone site</li>
			</ul>
			<p><strong>Correct Answer:</strong> develop at an interzone site</p>
			<p><em>Explanation:</em> Synovial joints develop at an interzone site, where mesenchymal cells differentiate into the structures that form the joint cavity and associated tissues.</p>
		</li>
		<li>
			<p>A __________________ is a slightly mobile joint.</p>
			<ul>
				<li>ellipsoidal</li>
				<li>amphiarthrosis</li>
				<li>diarthrosis</li>
				<li>pivot</li>
			</ul>
			<p><strong>Correct Answer:</strong> amphiarthrosis</p>
			<p><em>Explanation:</em> An amphiarthrosis is a type of joint that allows for limited movement, providing both stability and flexibility, such as the intervertebral discs.</p>
		</li>
		<li>
			<p>Which of the following contain channels within the bone matrix that house one of an osteocyte’s many cytoplasmic extensions that it uses to communicate and receive nutrients?</p>
			<ul>
				<li>diploë</li>
				<li>cartilage</li>
				<li>compact bone</li>
				<li>canaliculi</li>
			</ul>
			<p><strong>Correct Answer:</strong> canaliculi</p>
			<p><em>Explanation:</em> Canaliculi are small channels within the bone matrix that house the cytoplasmic extensions of osteocytes, allowing for communication and nutrient exchange.</p>
		</li>
	</ol>
