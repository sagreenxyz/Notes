	<h2>Chapter 21 - Questions</h2>
	<ol>
		<li>
			<p>Which structure allows lymph from the lower right limb to enter the bloodstream?</p>
			<ul>
				<li>right lymphatic duct</li>
				<li>left lymphatic trunk</li>
				<li>right lymphatic trunk</li>
				<li>thoracic duct</li>
			</ul>
			<p><strong>Correct Answer:</strong> thoracic duct</p>
			<p><strong>Explanation:</strong> The thoracic duct is the main lymphatic vessel that drains lymph from the lower right limb and other parts of the body into the bloodstream.</p>
		</li>
		<li>
			<p>Which of the following cells would be most active in early, antiviral immune responses the first time one is exposed to a pathogen?</p>
			<ul>
				<li>macrophage</li>
				<li>neutrophil</li>
				<li>natural killer cell</li>
				<li>T cell</li>
			</ul>
			<p><strong>Correct Answer:</strong> natural killer cell</p>
			<p><strong>Explanation:</strong> Natural killer cells are part of the innate immune system and are among the first responders to viral infections, providing an early defense before the adaptive immune system is activated.</p>
		</li>
		<li>
			<p>Which of the lymphoid nodules is most likely to see food antigens first?</p>
			<ul>
				<li>tonsils</li>
				<li>Peyer’s patches</li>
				<li>bronchus-associated lymphoid tissue</li>
				<li>mucosa-associated lymphoid tissue</li>
			</ul>
			<p><strong>Correct Answer:</strong> tonsils</p>
			<p><strong>Explanation:</strong> Tonsils are located at the entrance of the digestive and respiratory tracts and are the first lymphoid tissues to encounter food antigens.</p>
		</li>
		<li>
			<p>Enhanced phagocytosis of a cell by the binding of a specific protein is called ________.</p>
			<ul>
				<li>complement activation</li>
				<li>endocytosis</li>
				<li>opsonization</li>
				<li>anaphylaxis</li>
			</ul>
			<p><strong>Correct Answer:</strong> opsonization</p>
			<p><strong>Explanation:</strong> Opsonization is the process by which pathogens are marked for ingestion and destruction by phagocytes through the binding of opsonins, such as antibodies or complement proteins.</p>
		</li>
		<li>
			<p>Which type of cancer is associated with HIV disease?</p>
			<ul>
				<li>melanoma</li>
				<li>Kaposi’s sarcoma</li>
				<li>renal cell carcinoma</li>
				<li>lymphoma</li>
			</ul>
			<p><strong>Correct Answer:</strong> Kaposi’s sarcoma</p>
			<p><strong>Explanation:</strong> Kaposi’s sarcoma is a type of cancer that is commonly associated with HIV infection and is caused by human herpesvirus 8 (HHV-8).</p>
		</li>
		<li>
			<p>Which of the following leads to the redness of inflammation?</p>
			<ul>
				<li>increased vascular permeability</li>
				<li>increased blood flow</li>
				<li>anaphylactic shock</li>
				<li>complement activation</li>
			</ul>
			<p><strong>Correct Answer:</strong> increased blood flow</p>
			<p><strong>Explanation:</strong> Increased blood flow to the affected area during inflammation causes redness, as more blood is delivered to the site of injury or infection.</p>
		</li>
		<li>
			<p>Which of the following signs is not characteristic of inflammation?</p>
			<ul>
				<li>cold</li>
				<li>swelling</li>
				<li>redness</li>
				<li>pain</li>
			</ul>
			<p><strong>Correct Answer:</strong> cold</p>
			<p><strong>Explanation:</strong> Inflammation is typically characterized by heat, swelling, redness, and pain. Cold is not a characteristic sign of inflammation.</p>
		</li>
		<li>
			<p>How does cyclosporine A work?</p>
			<ul>
				<li>suppresses neutrophils</li>
				<li>suppresses T cells</li>
				<li>suppresses macrophages</li>
				<li>suppresses antibodies</li>
			</ul>
			<p><strong>Correct Answer:</strong> suppresses T cells</p>
			<p><strong>Explanation:</strong> Cyclosporine A is an immunosuppressive drug that primarily suppresses the activity of T cells, which are crucial for the immune response.</p>
		</li>
		<li>
			<p>Which of the following terms means “many genes”?</p>
			<ul>
				<li>multiple alleles</li>
				<li>polygeny</li>
				<li>polymorphism</li>
				<li>polypeptide</li>
			</ul>
			<p><strong>Correct Answer:</strong> polygeny</p>
			<p><strong>Explanation:</strong> Polygeny refers to a trait that is influenced by multiple genes, as opposed to a single gene.</p>
		</li>
		<li>
			<p>The elimination of self-reactive thymocytes is called ________.</p>
			<ul>
				<li>clonal selection</li>
				<li>tolerance</li>
				<li>positive selection</li>
				<li>negative selection</li>
			</ul>
			<p><strong>Correct Answer:</strong> negative selection</p>
			<p><strong>Explanation:</strong> Negative selection is the process by which self-reactive thymocytes are eliminated in the thymus to prevent autoimmune responses.</p>
		</li>
		<li>
			<p>Which enzymes in macrophages are important for clearing intracellular bacteria?</p>
			<ul>
				<li>lysosomal</li>
				<li>metabolic</li>
				<li>nuclear</li>
				<li>mitochondrial</li>
			</ul>
			<p><strong>Correct Answer:</strong> lysosomal</p>
			<p><strong>Explanation:</strong> Lysosomal enzymes in macrophages are responsible for breaking down and digesting intracellular bacteria.</p>
		</li>
		<li>
			<p>What disease is associated with bone marrow transplants?</p>
			<ul>
				<li>diabetes mellitus type I</li>
				<li>melanoma</li>
				<li>graft-versus-host disease</li>
				<li>headache</li>
			</ul>
			<p><strong>Correct Answer:</strong> graft-versus-host disease</p>
			<p><strong>Explanation:</strong> Graft-versus-host disease (GVHD) is a condition that can occur after a bone marrow transplant, where the donor's immune cells attack the recipient's tissues.</p>
		</li>
		<li>
			<p>T cells that secrete cytokines that help antibody responses are called ________.</p>
			<ul>
				<li>thymocytes</li>
				<li>regulatory T cells</li>
				<li>Th1</li>
				<li>Th2</li>
			</ul>
			<p><strong>Correct Answer:</strong> Th2</p>
			<p><strong>Explanation:</strong> Th2 cells are a subset of T helper cells that secrete cytokines to help B cells produce antibodies.</p>
		</li>
		<li>
			<p>The taking in of antigen and digesting it for later presentation is called ________.</p>
			<ul>
				<li>antigen processing</li>
				<li>endocytosis</li>
				<li>antigen presentation</li>
				<li>exocytosis</li>
			</ul>
			<p><strong>Correct Answer:</strong> antigen processing</p>
			<p><strong>Explanation:</strong> Antigen processing involves the uptake and digestion of antigens by antigen-presenting cells for presentation to T cells.</p>
		</li>
		<li>
			<p>Which of the following cells is phagocytic?</p>
			<ul>
				<li>macrophage</li>
				<li>NK cell</li>
				<li>B cell</li>
				<li>plasma cell</li>
			</ul>
			<p><strong>Correct Answer:</strong> macrophage</p>
			<p><strong>Explanation:</strong> Macrophages are phagocytic cells that engulf and digest pathogens and cellular debris.</p>
		</li>
		<li>
			<p>Why is clonal expansion so important?</p>
			<ul>
				<li>to select for specific cells</li>
				<li>to secrete cytokines</li>
				<li>to increase the numbers of specific cells</li>
				<li>to kill target cells</li>
			</ul>
			<p><strong>Correct Answer:</strong> to increase the numbers of specific cells</p>
			<p><strong>Explanation:</strong> Clonal expansion is the process by which specific immune cells proliferate to increase their numbers, ensuring an effective immune response.</p>
		</li>
		<li>
			<p>Which type of T cell is most effective against viruses?</p>
			<ul>
				<li>Th2</li>
				<li>Th1</li>
				<li>cytotoxic T cells</li>
				<li>regulatory T cells</li>
			</ul>
			<p><strong>Correct Answer:</strong> cytotoxic T cells</p>
			<p><strong>Explanation:</strong> Cytotoxic T cells are specialized in killing virus-infected cells and are crucial for controlling viral infections.</p>
		</li>
		<li>
			<p>Removing functionality from a B cell without killing it is called ________.</p>
			<ul>
				<li>clonal deletion</li>
				<li>clonal selection</li>
				<li>clonal expansion</li>
				<li>clonal anergy</li>
			</ul>
			<p><strong>Correct Answer:</strong> clonal anergy</p>
			<p><strong>Explanation:</strong> Clonal anergy is the process by which B cells are rendered non-functional without being destroyed, preventing them from participating in immune responses.</p>
		</li>
		<li>
			<p>Which class of antibody crosses the placenta in pregnant women?</p>
			<ul>
				<li>IgM</li>
				<li>IgG</li>
				<li>IgE</li>
				<li>IgA</li>
			</ul>
			<p><strong>Correct Answer:</strong> IgG</p>
			<p><strong>Explanation:</strong> IgG is the only class of antibody that can cross the placenta, providing passive immunity to the fetus.</p>
		</li>
		<li>
			<p>Why do we have natural antibodies?</p>
			<ul>
				<li>immunity to transplants</li>
				<li>We don’t know why.</li>
				<li>immunity to environmental bacteria</li>
				<li>from clonal selection</li>
			</ul>
			<p><strong>Correct Answer:</strong> immunity to environmental bacteria</p>
			<p><strong>Explanation:</strong> Natural antibodies provide immunity to common environmental bacteria, offering a first line of defense against infections.</p>
		</li>
	</ol>
