    <h2>Chapter 13 - Questions</h2>
    <ol>
        <li>
            <p>Which lobe of the cerebral cortex is responsible for generating motor commands?</p>
            <ul>
                <li>parietal</li>
                <li>frontal</li>
                <li>occipital</li>
                <li>temporal</li>
            </ul>
            <p><strong>Correct Answer:</strong> frontal</p>
            <p><strong>Explanation:</strong> The frontal lobe contains the primary motor cortex, which is responsible for initiating voluntary motor commands.</p>
        </li>
        <li>
            <p>Which primary vesicle of the embryonic nervous system does not differentiate into more vesicles at the secondary stage?</p>
            <ul>
                <li>diencephalon</li>
                <li>prosencephalon</li>
                <li>rhombencephalon</li>
                <li>mesencephalon</li>
            </ul>
            <p><strong>Correct Answer:</strong> mesencephalon</p>
            <p><strong>Explanation:</strong> The mesencephalon remains undivided and develops into the midbrain in the adult brain.</p>
        </li>
        <li>
            <p>Which adult structure(s) arises from the diencephalon?</p>
            <ul>
                <li>pons and cerebellum</li>
                <li>midbrain, pons, medulla</li>
                <li>thalamus, hypothalamus, retina</li>
                <li>cerebrum</li>
            </ul>
            <p><strong>Correct Answer:</strong> thalamus, hypothalamus, retina</p>
            <p><strong>Explanation:</strong> The diencephalon gives rise to the thalamus, hypothalamus, and retina in the adult brain.</p>
        </li>
        <li>
            <p>Which non-nervous tissue develops from the neuroectoderm?</p>
            <ul>
                <li>craniofacial bone</li>
                <li>vertebral bone</li>
                <li>digestive lining</li>
                <li>respiratory mucosa</li>
            </ul>
            <p><strong>Correct Answer:</strong> craniofacial bone</p>
            <p><strong>Explanation:</strong> The neuroectoderm contributes to the development of craniofacial bones and other structures.</p>
        </li>
        <li>
            <p>Which of these structures is not under direct control of the peripheral nervous system?</p>
            <ul>
                <li>gastric plexus</li>
                <li>cervical plexus</li>
                <li>sympathetic chain ganglia</li>
                <li>trigeminal ganglion</li>
            </ul>
            <p><strong>Correct Answer:</strong> gastric plexus</p>
            <p><strong>Explanation:</strong> The gastric plexus is part of the enteric nervous system, which operates independently of the peripheral nervous system.</p>
        </li>
        <li>
            <p>Which structure is associated with the embryologic development of the peripheral nervous system?</p>
            <ul>
                <li>neural tube</li>
                <li>neuraxis</li>
                <li>neural crest</li>
                <li>rhombencephalon</li>
            </ul>
            <p><strong>Correct Answer:</strong> neural crest</p>
            <p><strong>Explanation:</strong> The neural crest gives rise to many structures of the peripheral nervous system, including sensory and autonomic ganglia.</p>
        </li>
        <li>
            <p>The ___________________ is the nucleus deep in the temporal lobe of the cerebrum that is related to memory and emotional behavior.</p>
            <ul>
                <li>medulla</li>
                <li>amygdala</li>
                <li>dura</li>
                <li>cranial</li>
            </ul>
            <p><strong>Correct Answer:</strong> amygdala</p>
            <p><strong>Explanation:</strong> The amygdala is involved in processing emotions and is critical for memory formation related to emotional events.</p>
        </li>
        <li>
            <p>What is the name for a bundle of axons within a nerve?</p>
            <ul>
                <li>fascicle</li>
                <li>epineurium</li>
                <li>tract</li>
                <li>nerve root</li>
            </ul>
            <p><strong>Correct Answer:</strong> fascicle</p>
            <p><strong>Explanation:</strong> A fascicle is a bundle of axons within a nerve, surrounded by connective tissue.</p>
        </li>
        <li>
            <p>What region of the diencephalon coordinates homeostasis?</p>
            <ul>
                <li>hypothalamus</li>
                <li>thalamus</li>
                <li>epithalamus</li>
                <li>subthalamus</li>
            </ul>
            <p><strong>Correct Answer:</strong> hypothalamus</p>
            <p><strong>Explanation:</strong> The hypothalamus regulates homeostasis by controlling body temperature, hunger, thirst, and other autonomic functions.</p>
        </li>
        <li>
            <p>What condition causes a stroke?</p>
            <ul>
                <li>infection of cerebral spinal fluid</li>
                <li>inflammation of meninges</li>
                <li>disruption of blood to the brain</li>
                <li>lumbar puncture</li>
            </ul>
            <p><strong>Correct Answer:</strong> disruption of blood to the brain</p>
            <p><strong>Explanation:</strong> A stroke occurs when blood flow to the brain is disrupted, leading to cell death in affected areas.</p>
        </li>
        <li>
            <p>Which cranial nerve(s) controls the functions in the head and neck?</p>
            <ul>
                <li>olfactory</li>
                <li>trochlear</li>
                <li>all of these</li>
                <li>glossopharyngeal</li>
            </ul>
            <p><strong>Correct Answer:</strong> all of these</p>
            <p><strong>Explanation:</strong> Several cranial nerves, including the olfactory, trochlear, and glossopharyngeal nerves, control various functions in the head and neck.</p>
        </li>
        <li>
            <p>Which ganglion is responsible for cutaneous sensations of the face?</p>
            <ul>
                <li>trigeminal ganglion</li>
                <li>vestibular ganglion</li>
                <li>otic ganglion</li>
                <li>geniculate ganglion</li>
            </ul>
            <p><strong>Correct Answer:</strong> trigeminal ganglion</p>
            <p><strong>Explanation:</strong> The trigeminal ganglion contains sensory neurons that transmit cutaneous sensations from the face to the brain.</p>
        </li>
        <li>
            <p>What level of the brain stem is the major input to the cerebellum?</p>
            <ul>
                <li>pons</li>
                <li>spinal cord</li>
                <li>midbrain</li>
                <li>medulla</li>
            </ul>
            <p><strong>Correct Answer:</strong> pons</p>
            <p><strong>Explanation:</strong> The pons serves as a major relay center, transmitting information from the brainstem to the cerebellum.</p>
        </li>
        <li>
            <p>What region of the spinal cord contains motor neurons that direct the movement of skeletal muscles?</p>
            <ul>
                <li>posterior horn</li>
                <li>lateral horn</li>
                <li>alar plate</li>
                <li>anterior horn</li>
            </ul>
            <p><strong>Correct Answer:</strong> anterior horn</p>
            <p><strong>Explanation:</strong> The anterior horn of the spinal cord contains motor neurons that control voluntary movements of skeletal muscles.</p>
        </li>
        <li>
            <p>Brodmannâ€™s areas map different regions of the ________ to particular functions.</p>
            <ul>
                <li>cerebral cortex</li>
                <li>basal forebrain</li>
                <li>cerebellum</li>
                <li>corpus callosum</li>
            </ul>
            <p><strong>Correct Answer:</strong> cerebral cortex</p>
            <p><strong>Explanation:</strong> Brodmann's areas are regions of the cerebral cortex defined based on their cytoarchitecture and associated functions.</p>
        </li>
        <li>
            <p>Aside from the nervous system, which other organ system develops out of the ectoderm?</p>
            <ul>
                <li>respiratory</li>
                <li>urinary</li>
                <li>digestive</li>
                <li>integumentary</li>
            </ul>
            <p><strong>Correct Answer:</strong> integumentary</p>
            <p><strong>Explanation:</strong> The ectoderm gives rise to the integumentary system, including the skin, hair, and nails.</p>
        </li>
        <li>
            <p>What blood vessel enters the cranium to supply the brain with fresh, oxygenated blood?</p>
            <ul>
                <li>common carotid artery</li>
                <li>internal carotid artery</li>
                <li>aorta</li>
                <li>jugular vein</li>
            </ul>
            <p><strong>Correct Answer:</strong> internal carotid artery</p>
            <p><strong>Explanation:</strong> The internal carotid artery supplies oxygenated blood to the brain, entering the cranium through the carotid canal.</p>
        </li>
        <li>
            <p>Which layer of the meninges surrounds and supports the sinuses that form the route through which blood drains from the CNS?</p>
            <ul>
                <li>arachnoid mater</li>
                <li>pia mater</li>
                <li>dura mater</li>
                <li>subarachnoid</li>
            </ul>
            <p><strong>Correct Answer:</strong> dura mater</p>
            <p><strong>Explanation:</strong> The dura mater is the outermost layer of the meninges and supports the venous sinuses that drain blood from the CNS.</p>
        </li>
        <li>
            <p>What type of glial cell is responsible for filtering blood to produce CSF at the choroid plexus?</p>
            <ul>
                <li>ependymal cell</li>
                <li>oligodendrocyte</li>
                <li>Schwann cell</li>
                <li>astrocyte</li>
            </ul>
            <p><strong>Correct Answer:</strong> ependymal cell</p>
            <p><strong>Explanation:</strong> Ependymal cells line the ventricles of the brain and filter blood to produce cerebrospinal fluid (CSF) at the choroid plexus.</p>
        </li>
        <li>
            <p>Which portion of the ventricular system is found within the diencephalon?</p>
            <ul>
                <li>third ventricle</li>
                <li>fourth ventricle</li>
                <li>cerebral aqueduct</li>
                <li>lateral ventricles</li>
            </ul>
            <p><strong>Correct Answer:</strong> third ventricle</p>
            <p><strong>Explanation:</strong> The third ventricle is located within the diencephalon and connects to the lateral ventricles via the interventricular foramina.</p>
        </li>
    </ol>
