		</li>
		<li>
			<p>Which muscle pair plays a role in respiration?</p>
			<ul>
				<li>semispinalis cervicis, semispinalis thoracis</li>
				<li>trapezius, rhomboids</li>
				<li>diaphragm, scalene</li>
				<li>intertransversarii, interspinales</li>
