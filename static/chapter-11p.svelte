                <li>extensor digitorum</li>
                <li>extensor minimus longus</li>
                <li>biceps femoris</li>
            </ul>
            <p><strong>Correct Answer:</strong> extensor minimus longus</p>
        </li>
        <li>
            <p>Which of the following is a prime mover in head flexion?</p>
