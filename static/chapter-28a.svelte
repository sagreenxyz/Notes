        <li>
            <p>Marfan syndrome is inherited in an autosomal dominant pattern. Which of the following is true?</p>
            <ul>
                <li>Female offspring are more likely to inherit the disease.</li>
                <li>Male offspring are more likely to inherit the disease.</li>
                <li>Female offspring are more likely to be carriers of the disease.</li>
