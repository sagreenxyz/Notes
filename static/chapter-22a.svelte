	<h2>Chapter 22 - Questions</h2>
	<ol>
		<li>
			<p>A full complement of mature alveoli are present by ________.</p>
			<ul>
				<li>birth</li>
				<li>early childhood, around 8 years of age</li>
				<li>37 weeks</li>
				<li>16 weeks</li>
			</ul>
			<p><strong>Correct Answer:</strong> early childhood, around 8 years of age</p>
			<p><strong>Explanation:</strong> Alveoli continue to develop and mature after birth, reaching their full complement by early childhood.</p>
		</li>
		<li>
			<p>Which of the following are structural features of the trachea?</p>
			<ul>
				<li>C-shaped cartilage</li>
				<li>all of these</li>
				<li>smooth muscle fibers</li>
				<li>cilia</li>
			</ul>
			<p><strong>Correct Answer:</strong> all of these</p>
			<p><strong>Explanation:</strong> The trachea is supported by C-shaped cartilage, contains smooth muscle fibers, and is lined with cilia to help filter and move particles out of the respiratory tract.</p>
		</li>
		<li>
			<p>A section of the lung that receives its own tertiary bronchus is called the ________.</p>
			<ul>
				<li>respiratory segment</li>
				<li>bronchopulmonary segment</li>
				<li>pulmonary lobule</li>
				<li>interpulmonary segment</li>
			</ul>
			<p><strong>Correct Answer:</strong> bronchopulmonary segment</p>
			<p><strong>Explanation:</strong> Each bronchopulmonary segment is a distinct region of the lung that receives air from its own tertiary bronchus.</p>
		</li>
		<li>
			<p>The ______________________ is a(n) opening that allows airflow between neighboring alveoli:</p>
			<ul>
				<li>alveolar duct</li>
				<li>alveolar macrophage</li>
				<li>alveolar sac</li>
				<li>alveolar pore</li>
			</ul>
			<p><strong>Correct Answer:</strong> alveolar pore</p>
			<p><strong>Explanation:</strong> Alveolar pores, also known as pores of Kohn, allow for the movement of air between adjacent alveoli, helping to equalize pressure and provide collateral ventilation.</p>
		</li>
		<li>
			<p>The _________________ system consists of organs and structures that allow us to breathe by taking in oxygen and expelling carbon dioxide.</p>
			<ul>
				<li>bronchial</li>
				<li>respiratory</li>
				<li>interlobular</li>
				<li>pulmonary</li>
			</ul>
			<p><strong>Correct Answer:</strong> respiratory</p>
			<p><strong>Explanation:</strong> The respiratory system includes the lungs, airways, and muscles involved in breathing, facilitating the exchange of oxygen and carbon dioxide.</p>
		</li>
		<li>
			<p>When do fetal breathing movements begin?</p>
			<ul>
				<li>approximately week 37</li>
				<li>about week 16</li>
				<li>after birth</li>
				<li>approximately week 20</li>
			</ul>
			<p><strong>Correct Answer:</strong> approximately week 20</p>
			<p><strong>Explanation:</strong> Fetal breathing movements, which are practice movements of the respiratory muscles, typically begin around the 20th week of gestation.</p>
		</li>
		<li>
			<p>What is the function of the conchae in the nasal cavity?</p>
			<ul>
				<li>maintain surface tension</li>
				<li>increase surface area</li>
				<li>exchange gases</li>
				<li>maintain air pressure</li>
			</ul>
			<p><strong>Correct Answer:</strong> increase surface area</p>
			<p><strong>Explanation:</strong> The conchae, or turbinates, are bony structures in the nasal cavity that increase the surface area, helping to warm, humidify, and filter the air we breathe.</p>
		</li>
		<li>
			<p>Gas flow decreases as ________ increases.</p>
			<ul>
				<li>airway diameter</li>
				<li>friction</li>
				<li>pressure</li>
				<li>resistance</li>
			</ul>
			<p><strong>Correct Answer:</strong> resistance</p>
			<p><strong>Explanation:</strong> As resistance in the airways increases, gas flow decreases, making it more difficult for air to move through the respiratory tract.</p>
		</li>
		<li>
			<p>What happens to the fluid that remains in the lungs after birth?</p>
			<ul>
				<li>It lubricates the pleurae.</li>
				<li>It is expelled shortly after birth.</li>
				<li>It is absorbed shortly after birth.</li>
				<li>It reduces the surface tension of the alveoli.</li>
			</ul>
			<p><strong>Correct Answer:</strong> It is absorbed shortly after birth.</p>
			<p><strong>Explanation:</strong> The fluid that remains in the lungs after birth is typically absorbed by the body within a few hours, allowing the lungs to fully expand and function properly.</p>
		</li>
		<li>
			<p>If a baby is born prematurely before type II cells produce sufficient pulmonary surfactant, which of the following might you expect?</p>
			<ul>
				<li>no difficulty as type III cells can provide enough surfactant for normal breathing</li>
				<li>difficulty with pulmonary capillary flow</li>
				<li>difficulty expressing fluid</li>
				<li>difficulty inflating the lungs</li>
			</ul>
			<p><strong>Correct Answer:</strong> difficulty inflating the lungs</p>
			<p><strong>Explanation:</strong> Pulmonary surfactant, produced by type II cells, reduces surface tension in the alveoli, making it easier for the lungs to inflate. Without sufficient surfactant, premature infants may struggle to breathe.</p>
		</li>
		<li>
			<p>Contraction of the external intercostal muscles causes which of the following to occur?</p>
			<ul>
				<li>The diaphragm moves downward.</li>
				<li>The ribs and sternum move upward.</li>
				<li>The rib cage is compressed.</li>
				<li>The thoracic cavity volume decreases.</li>
			</ul>
			<p><strong>Correct Answer:</strong> The ribs and sternum move upward.</p>
			<p><strong>Explanation:</strong> When the external intercostal muscles contract, they lift the ribs and sternum upward and outward, increasing the volume of the thoracic cavity and allowing air to be drawn into the lungs.</p>
		</li>
		<li>
			<p>Exercise can trigger symptoms of AMS due to which of the following?</p>
			<ul>
				<li>abnormal neural signals</li>
				<li>small venous reserve of oxygen</li>
				<li>low partial pressure of oxygen</li>
				<li>low atmospheric pressure</li>
			</ul>
			<p><strong>Correct Answer:</strong> small venous reserve of oxygen</p>
			<p><strong>Explanation:</strong> Acute mountain sickness (AMS) can be triggered by exercise because the body has a limited reserve of oxygen in the veins, making it harder to meet the increased oxygen demand at high altitudes.</p>
		</li>
		<li>
			<p>Which of the following prevents the alveoli from collapsing?</p>
			<ul>
				<li>expiratory reserve volume</li>
				<li>residual volume</li>
				<li>tidal volume</li>
				<li>inspiratory reserve volume</li>
			</ul>
			<p><strong>Correct Answer:</strong> residual volume</p>
			<p><strong>Explanation:</strong> Residual volume is the amount of air that remains in the lungs after a forceful exhalation, preventing the alveoli from collapsing and allowing continuous gas exchange.</p>
		</li>
		<li>
			<p>Gas exchange that occurs at the level of the tissues is called ________.</p>
			<ul>
				<li>external respiration</li>
				<li>interpulmonary respiration</li>
				<li>internal respiration</li>
				<li>pulmonary ventilation</li>
			</ul>
			<p><strong>Correct Answer:</strong> internal respiration</p>
			<p><strong>Explanation:</strong> Internal respiration refers to the exchange of gases between the blood and the tissues, where oxygen is delivered to the cells and carbon dioxide is removed.</p>
		</li>
		<li>
			<p>Oxyhemoglobin forms by a chemical reaction between which of the following?</p>
			<ul>
				<li>carbonic anhydrase and carbon dioxide</li>
				<li>carbonic anhydrase and oxygen</li>
				<li>hemoglobin and oxygen</li>
				<li>hemoglobin and carbon dioxide</li>
			</ul>
			<p><strong>Correct Answer:</strong> hemoglobin and oxygen</p>
			<p><strong>Explanation:</strong> Oxyhemoglobin is formed when oxygen binds to hemoglobin in the red blood cells, allowing oxygen to be transported throughout the body.</p>
		</li>
		<li>
			<p>Which of the following occurs during the chloride shift?</p>
			<ul>
				<li>Bicarbonate is removed from the erythrocyte.</li>
				<li>Chloride is exchanged for bicarbonate.</li>
				<li>Bicarbonate is removed from the blood.</li>
				<li>Chloride is removed from the erythrocyte.</li>
			</ul>
			<p><strong>Correct Answer:</strong> Chloride is exchanged for bicarbonate.</p>
			<p><strong>Explanation:</strong> The chloride shift is a process in which chloride ions move into red blood cells as bicarbonate ions move out, helping to maintain the acid-base balance in the blood.</p>
		</li>
		<li>
			<p>Which of the following processes does atmospheric pressure play a role in?</p>
			<ul>
				<li>production of pulmonary surfactant</li>
				<li>pulmonary ventilation</li>
				<li>surface tension</li>
				<li>resistance</li>
			</ul>
			<p><strong>Correct Answer:</strong> pulmonary ventilation</p>
			<p><strong>Explanation:</strong> Atmospheric pressure is crucial for pulmonary ventilation, as it creates the pressure gradient that allows air to flow into and out of the lungs during breathing.</p>
		</li>
		<li>
			<p>A low partial pressure of oxygen promotes hemoglobin binding to carbon dioxide. This is an example of the ________.</p>
			<ul>
				<li>Henry’s law</li>
				<li>Dalton’s law</li>
				<li>Haldane effect</li>
				<li>Bohr effect</li>
			</ul>
			<p><strong>Correct Answer:</strong> Haldane effect</p>
			<p><strong>Explanation:</strong> The Haldane effect describes how low oxygen levels in the blood enhance the ability of hemoglobin to bind to carbon dioxide, facilitating the transport of carbon dioxide from the tissues to the lungs.</p>
		</li>
		<li>
			<p>Increased ventilation that results in an increase in blood pH is called ________.</p>
			<ul>
				<li>hyperpnea</li>
				<li>acclimatization</li>
				<li>apnea</li>
				<li>hyperventilation</li>
			</ul>
			<p><strong>Correct Answer:</strong> hyperventilation</p>
			<p><strong>Explanation:</strong> Hyperventilation is characterized by rapid and deep breathing, which can lead to a decrease in carbon dioxide levels in the blood and an increase in blood pH.</p>
		</li>
		<li>
			<p>The ________________________ is a tube connected to the trachea that branches into many subsidiaries and provides a passageway for air to enter and leave the lungs.</p>
			<ul>
				<li>illiac</li>
				<li>trachea</li>
				<li>bronchus</li>
				<li>aorta</li>
			</ul>
			<p><strong>Correct Answer:</strong> bronchus</p>
			<p><strong>Explanation:</strong> The bronchus is a major airway that branches off from the trachea and leads to the lungs, where it further divides into smaller airways to facilitate the movement of air.</p>
		</li>
	</ol>
