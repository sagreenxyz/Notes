            <p>Which of these physiological changes would not be considered part of the sympathetic fight-or-flight response?</p>
            <ul>
                <li>increased heart rate</li>
                <li>increased sweating</li>
                <li>increased stomach motility</li>
                <li>dilated pupils</li>
            </ul>
            <p><strong>Correct Answer:</strong> increased stomach motility</p>
            <p><strong>Explanation:</strong> The sympathetic response decreases stomach motility to conserve energy for critical functions like increased heart rate and pupil dilation.</p>
