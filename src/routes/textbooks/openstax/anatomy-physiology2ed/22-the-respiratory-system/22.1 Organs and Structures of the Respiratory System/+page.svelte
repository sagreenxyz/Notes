            <div class="side-note-container">
                <aside class="side-note right">
                    <strong>External Nose:</strong> (1) root, (2) bridge, (3) dorsum nasi, (4) apex.
                </aside>
                <p>The external nose consists of the surface and skeletal structures that result in the outward appearance of the nose and contribute to its numerous functions (Figure 22.3). The root is the region of the nose located between the eyebrows.</p>
            </div>
            <p>The bridge is the part of the nose that connects the root to the rest of the nose. The dorsum nasi is the length of the nose. The apex is the tip of the nose.</p>
            <div class="side-note-container">
                <aside class="side-note right">
                    <strong>Inferior Components (External Nose):</strong> (1) ala, (2) philtrum, (3) naris.
