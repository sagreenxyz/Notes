            <h3>The Nose and its Adjacent Structures</h3>
            <div class="side-note-container">
                <aside class="side-note right">
                    <strong>Nose:</strong> (1) external, and (2) internal.
                </aside>
                <p>The major entrance and exit for the respiratory system is through the nose. When discussing the nose, it is helpful to divide it into two major sections: the external nose, and the nasal cavity or internal nose.</p>
