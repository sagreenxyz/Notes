            <p>Underneath the thin skin of the nose are its skeletal features (see Figure 22.3, lower illustration). While the root and bridge of the nose consist of bone, the protruding portion of the nose is composed of cartilage. 
            <div class="side-note-container">
                <aside class="side-note right">
