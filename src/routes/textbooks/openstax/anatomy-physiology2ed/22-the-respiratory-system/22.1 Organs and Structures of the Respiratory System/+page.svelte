                <p>On either side of the apex, the nostrils are formed by the alae (singular = ala). An ala is a cartilaginous structure that forms the lateral side of each naris (plural = nares), or nostril opening. The philtrum is the concave surface that connects the apex of the nose to the upper lip.</p>
